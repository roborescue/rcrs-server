package rescuecore2.standard.messages;

/**
   URNs for standard messages.
 */
public enum StandardMessageURN {
    /** Move command. */
    AK_MOVE,
    /** Load command. */
    AK_LOAD,
    /** Unload command. */
    AK_UNLOAD,
    /** Say command. */
    AK_SAY,
    /** Tell command. */
    AK_TELL,
    /** Extinguish command. */
    AK_EXTINGUISH,
    /** Rescue command. */
    AK_RESCUE,
    /** Clear command. */
    AK_CLEAR,

    /** Channel subscribe command. */
    AK_SUBSCRIBE,

    /** Channel speak command. */
    AK_SPEAK
}